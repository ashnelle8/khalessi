#include "./includes/framebuffer.h"

void my_draw_line20(framebuffer_t *k, sfVector2u p, unsigned int size)
{
    int j;
    for (j = 0; j < size; j++) {
        p.y = p.x + j;
        my_put_pixel(k, p.x, p.y, sfYellow);
    }

}

